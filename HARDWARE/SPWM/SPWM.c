/* ========== 内部控制变量 ========== */
static u32 phase_accumulator = 0;    // 相位累加器 (Q24格式)
static u32 phase_increment = 0;      // 相位增量
static u16 inverter_duty = 1000;     // 调制深度 (0~1000, 对应0~100%)

/**
 * @brief  初始化SPWM (默认50Hz / 220Vrms)
 */
void SPWM_Init(void)
{
    SPWM_SetFrequency(50);
    SPWM_SetVoltage(220);
    phase_accumulator = 0;
}

/**
 * @brief  设置输出电压 (Vrms)
 * @param  voltage_V: 目标电压有效值 (0~230V)
 * @note   调制比 m = V_out * 2*sqrt(2) / V_dc
 */
void SPWM_SetVoltage(u16 voltage_V)
{
    if (voltage_V > 230) voltage_V = 230;

    float modulation = (voltage_V * 2.828f) / DC_BUS_VOLTAGE;
    if (modulation > 1.0f) modulation = 1.0f;

    inverter_duty = (u16)(modulation * 1000.0f);
}

/**
 * @brief  设置输出频率 (Hz)
 * @param  freq_Hz: 目标频率 (1~400Hz)
 * @note   相位增量 = F_out * N * 2^Q / F_carrier
 */
void SPWM_SetFrequency(u16 freq_Hz)
{
    if (freq_Hz > 400) freq_Hz = 400;
    if (freq_Hz < 1) freq_Hz = 1;

    uint64_t temp = (uint64_t)freq_Hz * TABLE_SIZE * FREQ_SCALE;
    phase_increment = (uint32_t)(temp / CARRIER_FREQ);
}

/**
 * @brief  SPWM更新 (需在PWM中断中周期调用)
 * @note   使用TIM1 CH1输出SPWM
 *         启用前需在main.c中初始化PWM1
 */
void SPWM_Update(void)
{
    phase_accumulator += phase_increment;

    u32 index = (phase_accumulator >> Q_SHIFT) % TABLE_SIZE;

    u16 pwm_value = (uint16_t)((sinData[index] - 1800) * inverter_duty / 1000 + 1800);
    TIM_SetCompare1(TIM1, pwm_value);
}
